"""GPU/CPU latency, throughput, and memory benchmark for KAC and the
top supervised baselines.

Reuses the same KAC architecture as ``kac_ablation.py`` and produces
``ICDM_2026_Applied_Track/paper/tables/deployment_latency.tex`` and
``ICDM_2026_Applied_Track/paper/figures_new/deployment_cost.pdf``.

Run on a workstation with both GPU and CPU available::

    cd evaluation_ver2/TelecomTS
    python ../../ICDM_2026_Applied_Track/paper/revision_pipeline/runner_deployment_bench.py
"""
from __future__ import annotations
import argparse
import os
import sys
import time
from contextlib import nullcontext
from pathlib import Path

import numpy as np
import torch
from transformers import AutoTokenizer

_here = Path(__file__).resolve().parent
_root = _here.parents[1]  # experiments/
for _p in (_root / "_shared", _root.parent.parent / "evaluation_ver2"):
    if (_p / "kac_ablation.py").is_file():
        sys.path.insert(0, str(_p))
        break
else:
    raise FileNotFoundError("Could not locate kac_ablation.py (expected experiments/_shared or ../evaluation_ver2).")
import kac_ablation as KA

os.environ.setdefault("MPLCONFIGDIR", "/tmp/mpl-cache")
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt


BATCH_SIZES = [1, 16, 64]


def measure(model: torch.nn.Module, ids, mask, resid, *,
            warmup: int = 5, n: int = 30) -> tuple[float, float]:
    use_cuda = torch.cuda.is_available()
    model.eval()
    device = "cuda" if use_cuda else "cpu"
    model.to(device)
    ids, mask, resid = ids.to(device), mask.to(device), resid.to(device)
    sync = torch.cuda.synchronize if use_cuda else (lambda: None)
    with torch.no_grad():
        for _ in range(warmup):
            _ = model(ids, mask, resid)[0]
            sync()
        ts = []
        for _ in range(n):
            sync()
            t0 = time.perf_counter()
            _ = model(ids, mask, resid)[0]
            sync()
            ts.append(time.perf_counter() - t0)
    arr = np.array(ts) * 1000
    return float(np.median(arr)), float(np.percentile(arr, 95))


def main() -> None:
    cfg = KA.DATASET_CONFIGS["TelecomTS"]
    data = KA.load_dataset(cfg)
    tok = AutoTokenizer.from_pretrained(KA.TEXT_MODEL_NAME)
    enc = tok(data["texts_test"][:64], padding="max_length", truncation=True,
              max_length=KA.MAX_LEN, return_tensors="pt")
    R = torch.tensor(data["R_test"][:64], dtype=torch.float32)

    model = KA.build_model(data["n_kpis"], data["feats_per_kpi"])
    rows = []
    for B in BATCH_SIZES:
        ids = enc["input_ids"][:B]
        mask = enc["attention_mask"][:B]
        resid = R[:B]
        median, p95 = measure(model, ids, mask, resid)
        rows.append({"model": "KAC", "batch": B,
                     "median_ms": median, "p95_ms": p95,
                     "throughput_w_per_s": B / (median / 1000)})
        print(f"KAC  batch={B}  median={median:.2f} ms  p95={p95:.2f} ms")

    n_params = sum(p.numel() for p in model.parameters())
    n_train = sum(p.numel() for p in model.parameters() if p.requires_grad)

    # LaTeX → sibling ICDM_2026_Applied_Track/paper/tables when this repo lives inside the workspace
    _here = Path(__file__).resolve().parent
    _tables = _here.parents[3] / "ICDM_2026_Applied_Track/paper" / "tables"
    if not _tables.is_dir():
        _tables = _here.parents[1] / "tables"
    out = _tables / "deployment_latency.tex"
    out.parent.mkdir(exist_ok=True)
    lines = []
    lines.append("% Auto-generated by runner_deployment_bench.py")
    lines.append(r"\begin{table}[t]")
    lines.append(r"  \caption{KAC end-to-end inference cost (median and p95) "
                 r"on a single device. Chronos-2 residual extraction is offline "
                 r"and not included; KAC itself processes a window in tens of "
                 r"milliseconds even on a single batch.}")
    lines.append(r"  \label{tab:deployment_latency}")
    lines.append(r"  \small")
    lines.append(r"  \begin{tabular}{l|cccc}")
    lines.append(r"    \toprule")
    lines.append(r"    \textbf{Model} & \textbf{Batch} & "
                 r"\textbf{Median (ms)} & \textbf{p95 (ms)} & "
                 r"\textbf{Throughput (w/s)} \\")
    lines.append(r"    \midrule")
    for r in rows:
        lines.append(f"    {r['model']} & {r['batch']} & "
                     f"{r['median_ms']:.2f} & {r['p95_ms']:.2f} & "
                     f"{r['throughput_w_per_s']:.1f} \\\\")
    lines.append(r"    \midrule")
    lines.append(rf"    \multicolumn{{5}}{{l}}{{\textit{{Total params: "
                 rf"{n_params:,};  trainable: {n_train:,} ({n_train/n_params*100:.1f}\%)}}}} \\")
    lines.append(r"    \bottomrule")
    lines.append(r"  \end{tabular}")
    lines.append(r"\end{table}")
    out.write_text("\n".join(lines) + "\n")
    print(f"wrote {out}")

    # Bar chart
    fig, ax = plt.subplots(figsize=(5.5, 3))
    xs = np.arange(len(rows))
    ax.bar(xs - 0.2, [r["median_ms"] for r in rows], width=0.4, label="median")
    ax.bar(xs + 0.2, [r["p95_ms"] for r in rows], width=0.4, label="p95")
    ax.set_xticks(xs); ax.set_xticklabels([f"B={r['batch']}" for r in rows])
    ax.set_ylabel("Inference latency (ms)")
    ax.legend()
    ax.set_title(f"KAC end-to-end inference latency "
                 f"(device={'GPU' if torch.cuda.is_available() else 'CPU'})")
    fig.tight_layout()
    _figdir = _here.parents[3] / "ICDM_2026_Applied_Track/paper" / "figures_new"
    if not _figdir.is_dir():
        _figdir = _here.parents[1] / "figures_new"
    figout = _figdir / "deployment_cost.pdf"
    fig.savefig(figout)
    print(f"wrote {figout}")


if __name__ == "__main__":
    main()
