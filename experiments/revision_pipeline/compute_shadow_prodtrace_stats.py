#!/usr/bin/env python3
"""Summarise pre-computed ProdTrace-SA (Production) KAC ablation CSV for paper text.

Reads ``experiments/_shared/kac_ablation_prodtrace_sa.csv`` (4 variants × 5 seeds)
and writes ``ICDM_2026_Applied_Track/paper/tables/shadow_prodtrace_stats.tex`` with LaTeX
\\providecommand macros for wall-clock budget claims in shadow / pre-staging.

Run from repo root::

    python experiments/revision_pipeline/compute_shadow_prodtrace_stats.py

Or from this directory::

    python compute_shadow_prodtrace_stats.py
"""
from __future__ import annotations

import argparse
import csv
import json
import statistics
from collections import defaultdict
from pathlib import Path


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument(
        "--csv",
        type=Path,
        default=None,
        help="Path to kac_ablation_prodtrace_sa.csv (default: ../_shared/...)",
    )
    ap.add_argument(
        "--out-tex",
        type=Path,
        default=None,
        help="Output .tex path (default: ../../../ICDM_2026_Applied_Track/paper/tables/...)",
    )
    ap.add_argument("--out-json", type=Path, default=None)
    args = ap.parse_args()

    here = Path(__file__).resolve().parent
    repo = here.parents[1]  # kac-telecom-anomaly-detection/
    default_csv = repo / "experiments" / "_shared" / "kac_ablation_prodtrace_sa.csv"
    csv_path = args.csv or default_csv

    default_tex = (
        repo.parent / "ICDM_2026_Applied_Track/paper" / "tables" / "shadow_prodtrace_stats.tex"
    )
    out_tex = args.out_tex or default_tex

    rows = list(csv.DictReader(csv_path.open(newline="")))
    wall = [float(r["wall_seconds"]) for r in rows]
    by_variant: dict[str, list[float]] = defaultdict(list)
    f1_by_variant: dict[str, list[float]] = defaultdict(list)
    for r in rows:
        v = r["variant_name"]
        by_variant[v].append(float(r["wall_seconds"]))
        f1_by_variant[v].append(float(r["f1"]))

    total_s = sum(wall)
    summary = {
        "csv": str(csv_path),
        "n_rows": len(rows),
        "n_seeds": len({r["seed"] for r in rows}),
        "n_variants": len({r["variant_code"] for r in rows}),
        "wall_seconds_total": total_s,
        "wall_hours_total": total_s / 3600.0,
        "wall_seconds_mean_per_row": statistics.mean(wall),
        "f1_mean_by_variant": {
            k: round(statistics.mean(v), 4) for k, v in f1_by_variant.items()
        },
    }

    if args.out_json:
        args.out_json.write_text(json.dumps(summary, indent=2))
        print("wrote", args.out_json)

    h = total_s / 3600.0
    lines = [
        "% Auto-generated by compute_shadow_prodtrace_stats.py",
        r"\providecommand{\KACShadowProdtraceRuns}{" + str(len(rows)) + "}",
        r"\providecommand{\KACShadowProdtraceWallHours}{" + f"{h:.2f}" + "}",
        r"\providecommand{\KACShadowProdtraceWallKiloSec}{" + f"{total_s/1000:.2f}" + "}",
    ]
    out_tex.parent.mkdir(parents=True, exist_ok=True)
    out_tex.write_text("\n".join(lines) + "\n")
    print(json.dumps(summary, indent=2))
    print("wrote", out_tex)


if __name__ == "__main__":
    main()
